package Package97;

public class ReplaceOfString {

	public static void main(String[] args) {
		// String to String Replacement
		String s3= "Shayam ji ki Shu aaj tak Aaya na hisab";
		System.out.println(s3.replace("Shayam", "Ram"));
		
		//Character Replacement (Char to Char)
		String s4= "Ram Bhrose";
		System.out.println(s4.replace('R', 'N'));

	}

}
