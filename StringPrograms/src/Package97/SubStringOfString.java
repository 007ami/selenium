package Package97;

public class SubStringOfString {

	public static void main(String[] args) {
		// Start from the index number which is start from 0. and Print from given index number
		String d= "Aawarapan NDeewanapan";
		System.out.println(d.substring(11));
		
		// Print the string between the Begin index to End Index
		String s= "I am manual tester";
		System.out.println(s.substring(4, 11));

	}

}
